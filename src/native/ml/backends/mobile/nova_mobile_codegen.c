int nova_mobile_gen_network_layer(NovaMobileProject *proj,
                                  NovaMobilePlatform platform) {
  if (platform == NOVA_MOBILE_IOS || platform == NOVA_MOBILE_BOTH) {
    NovaGeneratedFile *f =
        add_file(proj, "ios/Network/APIClient.swift", "swift");
    set_code(
        f,
        "// APIClient.swift - Generated by Nova Mobile Codegen\n\n"
        "import Foundation\n\n"
        "actor APIClient {\n"
        "    static let shared = APIClient()\n"
        "    private let session: URLSession\n"
        "    private let baseURL: URL\n\n"
        "    init(baseURL: URL = URL(string: \"https://api.example.com\")!) {\n"
        "        self.baseURL = baseURL\n"
        "        let config = URLSessionConfiguration.default\n"
        "        config.timeoutIntervalForRequest = 30\n"
        "        self.session = URLSession(configuration: config)\n"
        "    }\n\n"
        "    func get<T: Decodable>(_ path: String) async throws -> T {\n"
        "        let url = baseURL.appendingPathComponent(path)\n"
        "        let (data, _) = try await session.data(from: url)\n"
        "        return try JSONDecoder().decode(T.self, from: data)\n"
        "    }\n\n"
        "    func post<T: Codable, R: Decodable>(_ path: String, body: T) "
        "async throws -> R {\n"
        "        let url = baseURL.appendingPathComponent(path)\n"
        "        var request = URLRequest(url: url)\n"
        "        request.httpMethod = \"POST\"\n"
        "        request.httpBody = try JSONEncoder().encode(body)\n"
        "        request.setValue(\"application/json\", forHTTPHeaderField: "
        "\"Content-Type\")\n"
        "        let (data, _) = try await session.data(for: request)\n"
        "        return try JSONDecoder().decode(R.self, from: data)\n"
        "    }\n"
        "}\n");
  }

  if (platform == NOVA_MOBILE_ANDROID || platform == NOVA_MOBILE_BOTH) {
    NovaGeneratedFile *f = add_file(
        proj, "android/app/src/main/java/network/ApiClient.kt", "kotlin");
    set_code(f,
             "// ApiClient.kt - Generated by Nova Mobile Codegen\n\n"
             "package network\n\n"
             "import kotlinx.coroutines.Dispatchers\n"
             "import kotlinx.coroutines.withContext\n"
             "import java.net.HttpURLConnection\n"
             "import java.net.URL\n\n"
             "object ApiClient {\n"
             "    private const val BASE_URL = \"https://api.example.com\"\n\n"
             "    suspend fun get(path: String): String = "
             "withContext(Dispatchers.IO) {\n"
             "        val url = URL(\"$BASE_URL/$path\")\n"
             "        val conn = url.openConnection() as HttpURLConnection\n"
             "        conn.requestMethod = \"GET\"\n"
             "        conn.connectTimeout = 30000\n"
             "        conn.inputStream.bufferedReader().readText()\n"
             "    }\n\n"
             "    suspend fun post(path: String, body: String): String = "
             "withContext(Dispatchers.IO) {\n"
             "        val url = URL(\"$BASE_URL/$path\")\n"
             "        val conn = url.openConnection() as HttpURLConnection\n"
             "        conn.requestMethod = \"POST\"\n"
             "        conn.doOutput = true\n"
             "        conn.setRequestProperty(\"Content-Type\", "
             "\"application/json\")\n"
             "        conn.outputStream.write(body.toByteArray())\n"
             "        conn.inputStream.bufferedReader().readText()\n"
             "    }\n"
             "}\n");
  }
  return 0;
}

