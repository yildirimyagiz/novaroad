int nova_mobile_gen_swift_model(NovaMobileProject *proj, const char *name,
                                const char *fields) {
  char fname[256];
  snprintf(fname, sizeof(fname), "ios/Models/%s.swift", name);
  NovaGeneratedFile *f = add_file(proj, fname, "swift");

  char buf[4096];
  snprintf(buf, sizeof(buf),
           "//\n// %s.swift - Generated by Nova Mobile Codegen\n//\n\n"
           "import Foundation\n\n"
           "struct %s: Codable, Identifiable, Hashable {\n"
           "    let id: UUID\n"
           "%s"
           "\n"
           "    init(%s) {\n"
           "        self.id = UUID()\n"
           "        // TODO: Initialize fields\n"
           "    }\n"
           "}\n",
           name, name, fields, fields);
  set_code(f, buf);
  return 0;
}

