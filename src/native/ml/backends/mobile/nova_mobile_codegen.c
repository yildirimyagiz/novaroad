NovaMobileProject *nova_mobile_create_project(const NovaMobileConfig *cfg) {
  NovaMobileProject *proj = calloc(1, sizeof(NovaMobileProject));
  snprintf(proj->project_dir, sizeof(proj->project_dir), "%s/%s",
           cfg->output_dir, cfg->app_name);
  return proj;
}

void nova_mobile_destroy_project(NovaMobileProject *proj) {
  if (!proj)
    return;
  for (int i = 0; i < proj->file_count; i++)
    free(proj->files[i].code);
  free(proj->files);
  free(proj);
}

