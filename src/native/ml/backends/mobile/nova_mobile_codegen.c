int nova_mobile_build_ios(const NovaMobileProject *proj) {
  char cmd[1024];
  snprintf(cmd, sizeof(cmd),
           "xcodebuild -project %s/ios/*.xcodeproj "
           "-scheme NovaApp -sdk iphoneos "
           "-configuration Release 2>&1",
           proj->project_dir);
  printf("🍎 Building iOS: %s\n", cmd);
  return system(cmd);
}

int nova_mobile_build_android(const NovaMobileProject *proj) {
  char cmd[1024];
  snprintf(cmd, sizeof(cmd), "cd %s/android && ./gradlew assembleRelease 2>&1",
           proj->project_dir);
  printf("🤖 Building Android: %s\n", cmd);
  return system(cmd);
}
