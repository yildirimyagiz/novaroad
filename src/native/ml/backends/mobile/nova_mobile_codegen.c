// ═══════════════════════════════════════════════════════════════════════════
// Write to Disk
// ═══════════════════════════════════════════════════════════════════════════

int nova_mobile_write_project(const NovaMobileProject *proj) {
  if (!proj || proj->file_count == 0)
    return -1;

  printf("📱 Writing mobile project: %s (%d files)\n", proj->project_dir,
         proj->file_count);

  for (int i = 0; i < proj->file_count; i++) {
    const NovaGeneratedFile *f = &proj->files[i];
    char path[1024];
    snprintf(path, sizeof(path), "%s/%s", proj->project_dir, f->filename);

    // Ensure parent directory exists
    char dir[1024];
    strncpy(dir, path, sizeof(dir));
    char *last_slash = strrchr(dir, '/');
    if (last_slash) {
      *last_slash = '\0';
      ensure_dir(dir);
    }

    FILE *fp = fopen(path, "w");
    if (!fp) {
      fprintf(stderr, "❌ Cannot write: %s\n", path);
      continue;
    }
    fwrite(f->code, 1, f->code_len, fp);
    fclose(fp);
    printf("  ✅ %s (%zu bytes)\n", f->filename, f->code_len);
  }

  printf("📱 Project written successfully!\n");
  return 0;
}

