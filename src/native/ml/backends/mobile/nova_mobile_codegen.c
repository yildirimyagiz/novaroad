static void ensure_dir(const char *path) {
  char tmp[512];
  snprintf(tmp, sizeof(tmp), "%s", path);
  for (char *p = tmp + 1; *p; p++) {
    if (*p == '/') {
      *p = '\0';
      mkdir(tmp, 0755);
      *p = '/';
    }
  }
  mkdir(tmp, 0755);
}

static NovaGeneratedFile *add_file(NovaMobileProject *proj,
                                   const char *filename, const char *lang) {
  if (proj->file_count >= proj->capacity) {
    proj->capacity = proj->capacity ? proj->capacity * 2 : 32;
    proj->files =
        realloc(proj->files, proj->capacity * sizeof(NovaGeneratedFile));
  }
  NovaGeneratedFile *f = &proj->files[proj->file_count++];
  memset(f, 0, sizeof(*f));
  strncpy(f->filename, filename, sizeof(f->filename) - 1);
  strncpy(f->language, lang, sizeof(f->language) - 1);
  return f;
}

static void set_code(NovaGeneratedFile *f, const char *code) {
  f->code_len = strlen(code);
  f->code = malloc(f->code_len + 1);
  memcpy(f->code, code, f->code_len + 1);
}

__attribute__((unused)) static void append_code(NovaGeneratedFile *f,
                                                const char *code) {
  size_t extra = strlen(code);
  f->code = realloc(f->code, f->code_len + extra + 1);
  memcpy(f->code + f->code_len, code, extra + 1);
  f->code_len += extra;
}

// ═══════════════════════════════════════════════════════════════════════════
